// string object

var GGF = new String("kamado nezuko") //membuat string object
console.log(GGF); //output : String("kamado nezuko")

console.log(typeof GGF); //output : object

// string primitive

var GGF = "kamado" + "nezuko"; // membuat primitive string

console.log(GGF); //output : kamado nezuko

console.log(typeof GGF); //output : string

// *note: sangat disarankan untuk tidak menggunakan string object